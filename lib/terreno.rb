class Terreno
    tamX=0
    tamY=0

    def setX(num)
        if(num>0)
            tamX=num
            return tamX
        else
            return nil
        end
    end

    def setY(num)
        if(num>0)
            tamY=num
            return tamY
        else
            return nil
        end
    end
end
    
    